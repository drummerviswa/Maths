                        The Department of Mathematics is one of the largest units of Anna University with a strength of 36 faculty members to cater for the needs of the Under-Graduate and Post-Graduate programmes in Science, Engineering and Technology. Almost all the faculty members are Ph.D. degree holders having received their doctoral degrees from leading institutes/universities.

                        The department was established in the year 1975 with M.Sc. programme in Mathematics. In 1979, Research programmes were started which are leading to M.Phil. and Ph.D. degrees in Mathematics. Our department focuses research on Algebra, Analysis, Applied Mathematics & Mechanics, Applied Probability & Statistics and Graph Theory & Combinatorics.

                        Subsequently in the year 1993, it started M.Sc. Mathematics with Diversification in Computer Science, which was renamed as M.Sc. (Computer Science) in 1997. From the academic year 2001, Five-year Integrated M.Sc. Programmes in Computer Science and Information Technology are being offered. These two programmes are also successfully offered by the Department of Mathematics and most of the students are placed in reputed IT companies across the world.

                        The Department of Mathematics is recognized as DST - FIST sponsored Department at Anna University. The Department of Mathematics was sanctioned Rs.52 Lakhs for five years by the Department of Science and Technology from 21.05.2014.

                        The department maintains a good reputation for research, teaching and individual growth and heartens the pursuit of academic excellence through the transmission, transaction and transformation of knowledge through the consistent performance of disciplined and dedicated staff members. Our faculty members have published research articles in leading high-quality journals with good h-index.
